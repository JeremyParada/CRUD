    private boolean validaObligatorios()
    {
        boolean valido = true;
        String validaText = "";
        if(txtRut.getText().trim().isEmpty())
        {
            valido = false;
            validaText += "Rut es obligaotrio \n";
        }
        if(txtNombre.getText().trim().isEmpty())
        {
            valido = false;
            validaText += "Nombre es obligatorio \n";
        }
        if(mapComunas.get(cboComuna.getSelectedItem().toString()) == 0)
        {
            valido = false;
            validaText += "Debe seleccionar una comuna\n";
        }
        if(!chkJava.isSelected())
        {
            valido = false;
            validaText += "Debe seleccionar al menos el lenguaje JAVA";   
        }
        if(!valido)
        {
            JOptionPane.showMessageDialog(null, validaText);
        }
        return valido;
    }
    private void btnGrabarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGrabarMouseClicked
        // TODO add your handling code here:
        if(validaObligatorios())
        {
            String rut = txtRut.getText();
            String nombre = txtNombre.getText();
            String comuna = cboComuna.getSelectedItem().toString();
            int id_comuna = mapComunas.get(cboComuna.getSelectedItem().toString());
            String estado = "";
            String lenguajes = "";
            Cliente cli = new Cliente();
            if(rdbSoltero.isSelected())
            {
                estado = "Soltero";
            }
            else
            {
                if(rdbCasado.isSelected())
                {
                    estado = "Casado";
                }
                else
                {
                    estado = "Union Civil";
                }
            }
            if(chkJava.isSelected())
            {
                lenguajes += "JAVA,";
            }
            if(chkCSharp.isSelected())
            {
                lenguajes += "C#,";
            }
            if(chkJS.isSelected())
            {
                lenguajes += "JA,";
            }
            cli.setRut(rut);
            cli.setNombre(nombre);
            cli.setComuna(comuna);
            cli.setId_comuna(id_comuna);
            cli.setEstadoCivil(estado);
            cli.setLenguajes(lenguajes);
            if(nCli.agregar(cli))
            {
                JOptionPane.showMessageDialog(null, "Dator Grabados!!");
                limpiarTodo();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "UPS!!! no se grabaron los datos!!");
                limpiarTodo();

            }
        }
        else
        {
            limpiarTodo();
        } 
    }//GEN-LAST:event_btnGrabarMouseClicked

    private void tblClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblClientesMouseClicked
        // TODO add your handling code here:
        int fila = tblClientes.getSelectedRow();
        if(fila != -1)
        {
            limpiarFormulario();
            String rut = tblClientes.getValueAt(fila, 0).toString();
            String nombre = tblClientes.getValueAt(fila, 1).toString();
            String comuna = tblClientes.getValueAt(fila, 2).toString();
            String estado = tblClientes.getValueAt(fila, 3).toString();
            String lenguajes = tblClientes.getValueAt(fila, 4).toString();
            String[] listaLenguajes = lenguajes.split(",");
            txtRut.setText(rut);
            txtNombre.setText(nombre);
            cboComuna.setSelectedItem(comuna);
            if(estado.equalsIgnoreCase("Casado"))
            {
                rdbCasado.setSelected(true);
            }
            else
            {
                if(estado.equalsIgnoreCase("Soltero"))
                {
                    rdbSoltero.setSelected(true);
                }
                else
                {
                    rdbUnionCivil.setSelected(true);
                }
            }
            for(String leng : listaLenguajes)
            {
                if(!leng.isEmpty())
                {
                    if(leng.equalsIgnoreCase("JAVA"))
                    {
                        chkJava.setSelected(true);
                    }
                    if(leng.equalsIgnoreCase("C#"))
                    {
                        chkCSharp.setSelected(true);
                    }
                    if(leng.equalsIgnoreCase("JS"))
                    {
                        chkJS.setSelected(true);
                    }
                
                
                }
            
            }
            txtRut.setEnabled(false);
            btnGrabar.setEnabled(false);
            btnModificar.setEnabled(true);
            btnEliminar.setEnabled(true);
        }
    }//GEN-LAST:event_tblClientesMouseClicked

