    private void llenarComunas() {
        negComuna nComuna = new negComuna();
        ArrayList<Comuna> lComuna = new ArrayList<Comuna>();
        lComuna = nComuna.listarTodo();
        
        
        cboComuna.addItem("Seleccione");
        mapComunas.put("Seleccione", 0);
        for(Comuna com : lComuna)
        {
            cboComuna.addItem(com.getNombre());
            mapComunas.put(com.getNombre(), com.getId_comuna());
        }
        
        
        
    }

    private void llenarTabla() {
        //Objetos para recibir los datos
        ArrayList<Cliente> lCli = new ArrayList<Cliente>();
        lCli = nCli.listarTodo();
        //Objetos para llenar la tabla
        DefaultTableModel modelo = (DefaultTableModel) tblClientes.getModel();
        Object[] fila = new Object[5];
        for(Cliente cli : lCli)
        {
            fila[0] = cli.getRut();
            fila[1] = cli.getNombre();
            fila[2] = cli.getComuna();
            fila[3] = cli.getEstadoCivil();
            fila[4] = cli.getLenguajes();
            modelo.addRow(fila);
        }
        tblClientes.setModel(modelo);
    }

    private void limpiarFormulario() {
        txtRut.setText("");
        txtNombre.setText("");
        cboComuna.setSelectedIndex(0);
        chkJava.setSelected(false);
        chkCSharp.setSelected(false);
        chkJS.setSelected(false);
        rdbSoltero.setSelected(true);
        txtRut.setEnabled(true);
        txtRut.requestFocus();
        btnGrabar.setEnabled(true);
        btnModificar.setEnabled(false);
        btnEliminar.setEnabled(false);
    }
